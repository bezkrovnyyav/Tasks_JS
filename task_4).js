/* 4. Якщо змінна a більше нуля - то в ggg запишемо функцію, яка виводить один!, 
  інакше запишемо функцію, яка виводить два!*/


let ggg;
const a = 5; // Змініть значення a, щоб перевірити умову

if (a > 0) {
    ggg = function() {
        console.log('Один!');
    };
} else {
    ggg = function() {
        console.log('Два!');
    };
}

// Приклад виклику змінної ggg
ggg();
